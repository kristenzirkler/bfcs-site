            <p><strong>Host a Fundraiser for BFCS</strong><br>
            Whether it’s a breakfast, lunch or cocktail hour meeting, your office or facility could be a convenient location for BFCS staff to make an informal presentation about ways that financial contributions can make a big difference in the lives of children available for foster homes. We need you to invite potential donors.</p>

            <p><strong>Sponsor a New BFCS Video</strong><br>
            Underwriters are needed to provide financial support for BFCS to update its 10-minute video presentation about the agency, its services and the desperate need for foster and adoptive homes in the Valley. Production and duplication costs will be approximately $10,000.</p>

            <p>Donors may contribute the entire amount or a portion of this production cost.</p>

            <p><strong>Support Miss Mary’s Closet</strong><br>
            We frequently serve children who have never received a single article of new clothing in their lives. For every $25 that is raised, one deserving child can receive a complete outfit of new clothing, shoes and undergarments through the Miss Mary’s Closet program.</p>

            <p>Children from economically disadvantaged households can qualify for the program, which accepts either new clothing or cash contributions so that new clothing can be purchased.</p>

            <p>This program also provides emergency clothing for foster children who have been removed from homes where they are in peril.</p>
